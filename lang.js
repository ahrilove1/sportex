let currentLang = localStorage.getItem('sportexLang') || 'en';

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('sportexLang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  document.querySelectorAll('[data-lkey]').forEach(el => {
    const key = el.getAttribute('data-lkey');
    if (LANG[lang] && LANG[lang][key]) {
      el.innerHTML = LANG[lang][key];
    }
  });
}

function toggleLang() {
  const next = currentLang === 'zh' ? 'en' : 'zh';
  applyLang(next);
  location.reload();
}

// Load custom labels from backend and merge into LANG
let labelsLoaded = (async () => {
  try {
    const r = await fetch('data/labels.json');
    const data = await r.json();
    (data.labels || []).forEach(l => {
      if (l.en) LANG.en[l.key] = l.en;
      if (l.zh) LANG.zh[l.key] = l.zh;
    });
  } catch (e) {
    console.warn('Failed to load custom labels, using defaults');
  }
})();

// Apply saved language on load
document.addEventListener('DOMContentLoaded', async () => {
  await labelsLoaded;
  applyLang(currentLang);
});
